package e_oop;

public class Calculator {

	
	double plus(double a, double b){
		double rs = a + b;
		return rs;
	}
	double minus(double a, double b){
		double rs = a - b;
		return rs;
	}
	double multy(double a, double b){
		double rs = a * b;
		return rs;
	}
	double division(double a, double b){
		double rs = (double)a / b;
		return rs;
	}
	double nmg(double a, double b){
		double rs = a % b;
		return rs;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
